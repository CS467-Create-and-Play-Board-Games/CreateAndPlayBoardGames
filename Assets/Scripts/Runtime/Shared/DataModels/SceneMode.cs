namespace Runtime.Shared.DataModels
{
    /// <summary>
    /// Enum representing the different modes a scene can be in.
    /// </summary>
    public enum SceneMode
    {
        /// <summary>
        /// Represents the menu scene mode.
        /// </summary>
        Menu,

        /// <summary>
        /// Represents the in-game scene mode.
        /// </summary>
        InGame,

        /// <summary>
        /// Represents the level editor scene mode.
        /// </summary>
        LevelEditor,

        /// <summary>
        /// Represents an undetected scene mode.
        /// </summary>
        SceneModeNotDetected
    }
}
